/*:
 ## 常量
 
 到目前为止，你每次命名值时都是这样操作的：
 ```
(示例): 
 let name = "Johnny"
 ```
 
 完成时，`name` _始终_是“Johnny”。
*/
let name = "Johnny"
//name = "John"
//: - experiment:(体验):  取消注释上面的代码行，这行代码会将新值赋给 `name`：你会看到什么错误？
/*:
 不能更改的值称为**常量**，因为它们的值是固定的。你在使用 `let` 声明值时，就表示该值永远不会更改。
 
 那通常很有用。有时，你不希望值更改，或者只想偶尔更改一下值。但是有时，你_确实_想要更改一些事情。那么，就需要**变量**，而不是常量。 
*/
