/*:
 ## 声明变量
 
 声明变量和声明常量几乎完全一样。只是不使用关键字 `let`，而是使用关键字 `var`：
 */
// Declaring a constant
let placeOfBirth = "新泽西"

// Declaring a variable
var currentLocation = "新泽西"

/*:
 声明变量后，可以为其赋新值：
*/
// Assigning a new value to a variable
currentLocation = "加利福尼亚"

/*:
 - experiment:(体验):  \
 在下面，添加一行代码，将新值赋给 `currentLocation`：
 */
// Assign a new value

currentLocation="佐治亚"
/*:
 在大多数情况下，常量和变量是相同的：
- 它们都使用相同的名称规则
- 都将名称与所赋的值相关联
- 都有特定类型
 
 区别在于：
 - 首次给常量赋值之后，*无法*再更改常量值。
 - 首次给变量赋值之后，*可以*再更改变量值。

接下来，我们来学习如何使用变量。
 */
