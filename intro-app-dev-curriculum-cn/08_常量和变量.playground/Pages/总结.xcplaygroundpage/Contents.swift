 ## 总结
 
 在此 playground 中，你已经学习了一些非常重要的概念：
 
 - 使用 `let` 声明的值是常量，在赋值之后就不能更改。这些值“不可变”。
 - 使用 `var` 声明的值是变量，可随时间的变化而赋新值。这些值“可变”。
 - 可变值可用在其本身的赋值语句中：`score = score + 10`。
 - 复合赋值运算符允许更新可变值：`score += 10`。
 - 正确使用常量和变量有助于确保代码更安全、更易于理解。
 
