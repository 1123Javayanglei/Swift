 ## 更改颜色

许多程序员喜欢以他们感觉舒服的形式设置电脑环境。在 Xcode 中，可从菜单栏选择“Xcode”>“Preferences...”来更改环境：

 ![“Xcode”菜单打开的菜单栏 ](xcode-preferences.png)

在“Preferences”窗口中，选择“Fonts &amp; Colors”选项卡，并确保选中“Source Editor”：

 ![显示“Fonts and Colors”选项卡的 Xcode 偏好设置窗格](xcode-pref-fonts-and-colors.png)


要感受满满的怀旧风，请从以下三个黑色背景的选项中任选一个：Civic、Dusk 或 Midnight：

 ![显示已选中“Dusk”选项的“Fonts and Colors”的 Xcode 偏好设置窗格](xcode-pref-dusk-color.png)


修改之后，阅读代码变得更费力了吗？没关系：这些都是属于你的偏好设置，你可以依照自己的喜好随意设置代码。但是，如果你还能够坚持使用此黑色背景阅读代码，请在接下来的几个 playground 页面中继续使用该设置，你还要在这些页面上再添加一些老式效果。
 
