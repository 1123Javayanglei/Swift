 ## 总结
 
 你可以使用数组来存放项目列表。数组有两个重要特性。
 
 - 数组中的项目都具有相同的类型。
 - 数组中的项目具有特定顺序。
 
 由于这两个特性，你可以使用索引，从数组的特定点访问项目，并且你始终会取回已知类型的值。
 
 以下是你了解到的有关数组的一些其他方面：
 
 - 数组中的第一个索引是零不是一。
 - 使用索引访问数组可能有危险。如果所使用的索引超出数组界限，程序将会崩溃。
 - 可以使用 `count` 属性来弄清楚数组中项目的数量：
 - 可以使用 `for...in` 循环，安全地按顺序访问数组中的每个项目，而无需知道数组包含多少项目。
 - 可变数组允许添加、移除和替换项目。
 
