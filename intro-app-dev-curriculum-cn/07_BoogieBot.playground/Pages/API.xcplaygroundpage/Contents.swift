 ## API
 
 App 开发者必然要用到其他人提供的代码。在 BoogieBot 这个例子中，定义了许多函数，让 BoogieBot 能够启动、移动以及更改颜色。
 
 控制 BoogieBot 的函数集是一种“应用程序编程界面”，通常称为 _API_。
 
 API 是软件开发者用来完成某项任务的一组特定功能。在本例中，BoogieBot API 能够让软件机器人跳舞。
 
  ### BoogieBot API
 启动 BoogieBot：
 - `startBot()`
 
 移动 BoogieBot：
 
 - `leftArmUp()`、`leftArmDown()`、`rightArmUp()`、`rightArmDown()`
 - `leftLegUp()`、`leftLegDown()`、`rightLegUp()`、`rightLegDown()`
 - `shakeItLeft()`、`shakeItRight()`、`shakeItCenter()`
 - `jumpUp()`、`jumpDown()`
 
 更改 BoogieBot 的颜色：
 - `fabulize()`、`defabulize()`
 
