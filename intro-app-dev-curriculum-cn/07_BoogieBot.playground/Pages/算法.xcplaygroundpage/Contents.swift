 ## 算法
 
 在本课中，你已经通过将动作组合到函数中，为 BoogieBot 创建了不同的舞蹈套路。计算机科学家会说你已经在本课中学习了建立“算法”。
 
建立算法就是定义一系列要执行的步骤。当然，这些步骤不一定是舞步；任何一套独立的逐步执行的操作都是算法。
 
定义算法在编程中占很大的比重。有些算法属于复杂的数学算法，例如，压缩视频，以便可以在互联网上高效地发送视频。有些算法根本不涉及数学，例如，检查 app 用户是否收到新信息并在收到信息时显示通知。当然，还有让软件机器人跳舞的算法。
 
