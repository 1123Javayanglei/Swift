//: ## 键入名称与自动填写
//: Xcode 会跟踪你已经定义的所有名称。因此，当你开始键入时，将会看到建议名称（已记录的名称）的菜单。这称为 _“自动填写”_。
//:
//: 自动填写有助于加快代码编写速度，同时减少键入错误。键入的内容越多，自动填写菜单滤出的寻找内容范围就越小。
//:
//: 无论何时显示自动填写菜单，你都可以：
//: - 继续键入以缩小列表。
//: - 使用上箭头键和下箭头键在列表中上下移动。
//: - 按 Return 键让 Xcode 填写所选择的名称。
//:
//: ![自动填写弹出式菜单](autocomplete.png)
//: 列表中会显示你未定义的名称。忽略这些名称就可以了。
//:
//: 在下一页的体验过程中，你将键入一些名称并看看自动填写如何操作。
