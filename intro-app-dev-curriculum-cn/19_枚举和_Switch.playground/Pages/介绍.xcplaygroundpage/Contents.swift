 ## 多种选择
 
 在生活中，你经常作出选择。在许多情况下，你必须挑选其中一组选项，不可以什么都不选。
 
 例如，某天在学校食堂，午餐选择可能是意大利面、汉堡或浓汤。食堂职员没有心情让你进行搭配，因此你必须选择其中一种。
 
 选择一项。 
 
 此系统非常适合食堂，而且对于学校：
 
 - 职员知道他们只需要提供三种食物中的一种。
 - 职员可以快速高效地准备并提供每一种餐食。
 - 没有时间用于理解或记录特殊订单。
 - 学校知道每个人都已经吃到了营养丰富的午餐，一下午的学习都将充满活力。
 
 你作为程序员，可以从在程序中应用这些相同类型的可控选项中获益良多。 

