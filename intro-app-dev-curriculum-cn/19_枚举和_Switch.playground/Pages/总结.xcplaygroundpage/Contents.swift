 ## 总结
 
 如果你想要呈现其中一组相关值，可以使用枚举。每个可能的值称为事例。
 
 创建枚举就是创建新类型。该类型的实例只能包含匹配其中一个指定事例的值。
 
 使用枚举可使代码更易于阅读和编写，因为它总是清楚地指明可能的值是什么，以及那些值的意义。
 
 你可以使用 `==` 来比较枚举值，或者使用 switch 语句来测试所有可能的值。
 
 与 if 语句一样，switch 语句也是一种在代码中做出决策的方法。Switch 语句与枚举是最佳拍档，但是可用于开启任何类型的值。
 
 因为 switch 语句必须详尽，所以你必须处理每个可能的值。如果尚未指定要处理的值，可以使用默认事例。 
 
 与结构一样，你可以向枚举添加计算属性和方法。
 
