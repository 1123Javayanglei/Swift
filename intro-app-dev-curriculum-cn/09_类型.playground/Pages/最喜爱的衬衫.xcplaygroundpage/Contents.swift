 ## 最喜爱的衬衫
 
 如果你花点时间想一件你最喜爱的衬衫，它不可能是香蕉。这没有任何意义。香蕉不是衬衫。
 
 另一方面，如果你想出一种最喜爱的水果，无论是香蕉、苹果还是什么，它肯定得是种水果。
 
 当人们为某件东西命名时（例如，“最喜爱的水果”），该名称通常会与特定类型的事物联系起来。
 
 “最喜爱的水果”这一概念只有在与某种水果联系起来时才有意义。与“活动扳手”或“天文学”联系起来则毫无意义。你的大脑能够精准确定名称是否与正确的事物类型相匹配。
 
 同样地，Swift 会跟踪与常量和变量相关联的值类型，并确保你不会误将本应是衬衫的值设置为香蕉。
 
