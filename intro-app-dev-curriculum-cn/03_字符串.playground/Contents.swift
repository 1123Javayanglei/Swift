//: ## 字符串
//: 进入任何一个 app，你都可以在 app 的某个位置看到文本。文本无处不在，消息 app、新闻阅读器、社交媒体 app，甚至相机 app 都会在某些按钮和控件中使用文本。在计算机科学中，文本值几乎一直被称为_字符串_。
//:
//: ### 为何将它们称为字符串？
//: 可以把它们想像成一串珠子。每个珠子看起来都不一样，甚至形状不同，但这些珠子都按特定的顺序排列。
//: ![一串珠子](StringOfBeads.png)
//: 通常由一个字符（比如字母“H”）组成的字符串没有什么实际用途。我们需要将一整串字符组合在一起，构成单词、句子、段落等等。因此，程序员将文本数据称为_“字符串”_。
//: ![字符组成的字符串](StringOfCharacters.png)
