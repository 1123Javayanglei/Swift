//: ## 字符
//: 当然，文本可以包含字母以外的元素，比如标点符号、数字、符号，甚至空格。所有这些都称为“字符”，字符串由字符组成。
//:
//: 字母是字符：
//: ## Aa Bb Cc Dd Ee
//: 标点符号：
//: ## ! ? . , ; :
//: 数字：
//: ## 1 2 3 4 5 6 7 8 9 0
//: 符号：
//: ## # @ $ & © π * + - /
//: 甚至空格和 Tab 键等不可见字符也是字符。\
//: （你看不到它们，但它们确实存在。）
//: ## `                         `
