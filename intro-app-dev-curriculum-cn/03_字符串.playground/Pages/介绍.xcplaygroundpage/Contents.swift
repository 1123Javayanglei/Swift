//: ## 字符串
//: 进入任何一个 app，你都可以看到文本。文本无处不在，包括消息 app、新闻阅读器、社交媒体 app 等等。甚至相机 app 也使用文本来标识按钮和控件。
//:
//: 在包括 Swift 在内的大多数编程语言中，文本值称为_字符串_。
//:
//: ### 为何将它们称为字符串？
//: 可以把它们想像成一串珠子。每个珠子看起来都不一样，但这些珠子都按特定的顺序排列在一起。
//:
//: ![一串珠子](StringOfBeads.png)
//: 通常，单独一个字母（比如“H”）没什么用处。单词由一串字母串组成。因此，程序员将文本称为“字符串”。字符串可以是你能够想像的任何文本。
//:
//: ![字符组成的字符串](StringOfCharacters.png)
//:
//: 接下来，我们来学习除了字母还有哪些元素可以组成字符串。
